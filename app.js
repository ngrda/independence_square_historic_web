function initNavbar() {
    const nav = document.getElementById("mainNav");
    window.addEventListener("scroll", () => {
        if (window.scrollY > 50) {
            nav.classList.add("nav-scrolled");
        } else {
            nav.classList.remove("nav-scrolled");
        }
    });
}

function initScrollReveal() {
    const revealElements = document.querySelectorAll(".scroll-reveal");
    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add("revealed");
            }
        });
    }, { threshold: 0.1, rootMargin: "0px 0px -40px 0px" });
    
    revealElements.forEach(el => observer.observe(el));
}

function initMetricsObserver() {
    const bar = document.getElementById("metricsBar");
    const numbers = document.querySelectorAll(".metric-number");
    
    if (!bar) return; // Fallback de seguridad si el ID no se encuentra en el DOM
    
    const observer = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                numbers.forEach(num => {
                    const target = parseInt(num.getAttribute("data-target"));
                    let current = 0;
                    
                    const duration = 4000; 
                    const frameRate = 1000 / 60; 
                    const totalFrames = Math.round(duration / frameRate);
                    let frame = 0;
                    
                    const ticker = setInterval(() => {
                        frame++;
                        const progress = frame / totalFrames;
                        const easeOutProgress = 1 - Math.pow(1 - progress, 4);
                        
                        current = target * easeOutProgress;
                        
                        if (frame >= totalFrames) {
                            num.innerText = target;
                            clearInterval(ticker);
                        } else {
                            num.innerText = Math.floor(current);
                        }
                    }, frameRate);
                });
                observer.unobserve(bar);
            }
        });
    }, { 
        threshold: 0.1,
        rootMargin: "0px 0px -50px 0px"
    });
    
    observer.observe(bar);
}

