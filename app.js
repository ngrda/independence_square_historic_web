function toggleMobileMenu() {
    const popup = document.getElementById("mobileNavPopup");
    if (!popup) return;
    popup.classList.toggle("popup-active");
    
    if (popup.classList.contains("popup-active")) {
        document.body.style.overflow = "hidden";
    } else {
        document.body.style.overflow = "";
    }
}

function handleMobileLinkClick() {
    toggleMobileMenu();
}

document.addEventListener("keydown", (e) => {
    if (e.key === "Escape") {
        closeModal();
        const popup = document.getElementById("mobileNavPopup");
        if (popup && popup.classList.contains("popup-active")) {
            toggleMobileMenu();
        }
    }
});

/**
 * Interactividad de Desplegables para la Sección de Trails
 */
function toggleTrailDropdown(button) {
    const isExpanded = button.getAttribute("aria-expanded") === "true";
    const detailsContainer = button.nextElementSibling;
    const icon = button.querySelector(".toggle-icon");
    const label = button.querySelector("span");

    if (!isExpanded) {
        button.setAttribute("aria-expanded", "true");
        detailsContainer.classList.add("active");
        icon.innerText = "−";
        icon.style.transform = "rotate(180deg)";
        label.innerText = "Hide Historic Milestones";
    } else {
        button.setAttribute("aria-expanded", "false");
        detailsContainer.classList.remove("active");
        icon.innerText = "+";
        icon.style.transform = "rotate(0deg)";
        label.innerText = "Show Historic Milestones";
    }
}